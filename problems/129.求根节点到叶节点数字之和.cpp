class Solution {
public:
    int sumNumbers(TreeNode* root) {
        dfs(root,0);
        return res;
    }
    void dfs(TreeNode* root, int num){
        if(!root){
            return ;
        }
        num = num*10 + root->val;
        if(!root->left && !root->right) res += num;
        dfs(root->left, num);
        dfs(root->right, num);
    }
    int res;
};
